public class revisão_for_break {
    public static void main(String[] args){

        for (int i = 0; i <=10; i++) {

            if(i==5){
                break; //quebra o laço de repetição
            }
            System.out.println(i); // imprime uma sequencia de 1 a 4 e quebra antes do 5, encerrando o código
        }
    }
}

