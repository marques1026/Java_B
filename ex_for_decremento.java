public class ex_for_decremento {
    public static void main(String[] args){

        for (int i = 10; i > 0; i--) { //cria um loop de iteração // -- é um decremento (diminui de 1 em 1)
            //start     //stop  //step

            System.out.println(i); //Vai imprimir números de 10 a 11
        }
    }
}

