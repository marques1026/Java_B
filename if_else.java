public class if_else {
    public static void main(String[] args) {
        double tempo = 91;

        //Blocos de códigos separados - Bloco A
        if(tempo > 90){
            System.out.println("O tempo foi maior que 90");
        }
        //Blocos de códigos separados - Bloco B
        else if (tempo > 60){
            System.out.println("O tempo foi maior que 60");
        }
        //Com a adição do else if, o código se torna um
    }
}

